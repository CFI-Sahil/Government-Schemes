// Get title from URL
const params = new URLSearchParams(window.location.search);
const schemeTitle = params.get("title");

// Find the matching scheme
const scheme = schemesData.find(s => s.title === schemeTitle);

// Target DOM elements
const titleEl = document.getElementById("scheme-title");
const introEl = document.getElementById("scheme-intro");
const eligibilityEl = document.getElementById("scheme-eligibility");
const videoEl = document.getElementById("scheme-video");
const linkEl = document.getElementById("scheme-link");
const youLi = document.getElementById("you-link");

if (scheme) {
  titleEl.textContent = scheme.title;
  introEl.textContent = scheme.intro;
  videoEl.src = scheme.youtubeLink;
  linkEl.href = scheme.applyLink;
  youLi.href = scheme.youtubeLink;

  // Clear existing eligibility list (if any)
  eligibilityEl.innerHTML = "";

  // Create list items for each eligibility point
  scheme.eligibility.forEach(point => {
    const li = document.createElement("li");
    li.textContent = point;
    eligibilityEl.appendChild(li);
  });
  const howToApplyEl = document.getElementById("how-to-app");
  scheme.howToApply.forEach(step => {
    const li = document.createElement("li");
    li.textContent = step;
    howToApplyEl.appendChild(li);
  });
} else {
  titleEl.textContent = "Scheme Not Found";
  document.querySelector(".app-container").innerHTML += "<p>The scheme you're looking for does not exist.</p>";
}


